#! /bin/sh

echo "------------------------------------------------------------------------------------------------------------------------"
echo "Downloading Python 2.7.11; ---------------------------------------------------------------------------------------------"
echo "------------------------------------------------------------------------------------------------------------------------"
curl -o .deploy/Python.pkg https://www.python.org/ftp/python/2.7.11/python-2.7.11-macosx10.6.pkg

echo "------------------------------------------------------------------------------------------------------------------------"
echo "Installing Python 2.7.11; ----------------------------------------------------------------------------------------------"
echo "------------------------------------------------------------------------------------------------------------------------"
sudo installer -package .deploy/Python.pkg -target /

echo "------------------------------------------------------------------------------------------------------------------------"
echo "Upgrading pip; ---------------------------------------------------------------------------------------------------------"
echo "------------------------------------------------------------------------------------------------------------------------"
sudo -H pip install --upgrade pip

echo "------------------------------------------------------------------------------------------------------------------------"
echo "Installing requests; ---------------------------------------------------------------------------------------------------"
echo "------------------------------------------------------------------------------------------------------------------------"
sudo -H pip install requests

echo "------------------------------------------------------------------------------------------------------------------------"
echo "Installing TravisPy; ---------------------------------------------------------------------------------------------------"
echo "------------------------------------------------------------------------------------------------------------------------"
sudo -H pip install travispy
